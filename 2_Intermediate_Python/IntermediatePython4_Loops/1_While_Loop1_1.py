# create a variable named offset with an initial value of 8.
# Code a while loop that keeps running as long as offset is not equal to 0.
# Inside the loop, print the sentence "correcting..."
# Next decrease the value of offset by 1.
# Still within your loop, print out offset to see how it changes.

# Initialize offset
offset = 8

# Code the while loop
while offset != 0:
    print('correcting...')
    offset = offset - 1
    print(offset)